package H071221009.Pertemuan_5;

public class KomunitasProgrammerUnhas {
    private String nama;
    private String hobby;

    public KomunitasProgrammerUnhas(String nama, String hobby) {
        this.nama = nama;
        this.hobby = hobby;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getHobby() {
        return hobby;
    }
    
    public void setHobby(String hobby) {
        this.hobby = hobby;
    }
}